import { readFileSync, writeFileSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const docsRoot = path.join(root, 'content', 'docs');
const meta = JSON.parse(readFileSync(path.join(docsRoot, 'meta.json'), 'utf8'));

function stripQuotes(value) {
  return value.replace(/^['"]|['"]$/g, '');
}

function parseFrontmatter(raw) {
  const match = raw.match(/^---\n([\s\S]*?)\n---\n?/);
  if (!match) {
    return { title: '', description: '', body: raw };
  }

  const frontmatter = match[1];
  const body = raw.slice(match[0].length);
  const title = stripQuotes(frontmatter.match(/^title:\s*(.*)$/m)?.[1] ?? '');
  const description = stripQuotes(
    frontmatter.match(/^description:\s*(.*)$/m)?.[1] ?? '',
  );

  return { title, description, body };
}

function connectorCatalogPage() {
  return {
    title: 'Connectors',
    description:
      'Browse every pm connector bundle by capability, stream, action, and machine-readable metadata.',
    url: '/docs/connectors',
    fileName: '',
    body: '',
  };
}

function pageFromMetaEntry(entry) {
  if (entry === '---') return null;
  if (/^[A-Z][A-Za-z ]+$/.test(entry)) return null;

  const link = entry.match(/^\[(.+)]\((.+)\)$/);
  if (link) {
    const [, title, url] = link;
    if (url === '/docs/connectors') return connectorCatalogPage();
    return { title, description: '', url, fileName: '', body: '' };
  }

  const fileName = `${entry}.mdx`;
  const raw = readFileSync(path.join(docsRoot, fileName), 'utf8');
  const parsed = parseFrontmatter(raw);
  return {
    title: parsed.title,
    description: parsed.description,
    url: entry === 'index' ? '/docs' : `/docs/${entry}`,
    fileName,
    body: parsed.body.trimStart(),
  };
}

const pages = meta.pages.map(pageFromMetaEntry).filter(Boolean);

const generated = `// Code generated by scripts/gen-docs-data.mjs; DO NOT EDIT.

export type DocsGeneratedPage = {
  title: string;
  description: string;
  url: string;
  fileName: string;
  body: string;
};

export const DOCS_PAGES: DocsGeneratedPage[] = ${JSON.stringify(pages, null, 2)};

export function docsPageByUrl(url: string): DocsGeneratedPage | undefined {
  return DOCS_PAGES.find((page) => page.url === url);
}
`;

writeFileSync(path.join(root, 'lib', 'docs.generated.ts'), generated);
console.log(`Wrote ${pages.length} docs pages to lib/docs.generated.ts.`);
