// Merges the per-slug verified enrichment files produced by the enrichment
// Workflow (.enrich/enr/<slug>.json) into the committed TS module
// website/lib/connectors.enrichment.generated.ts.
//
// Each input file is a ConnectorEnrichment object:
//   { prerequisites: string[], authMethods: {name,summary}[],
//     setupSteps: {title,body}[], sources: {title,url}[] }
//
// Run: node scripts/merge-enrichment.mjs [ENR_DIR]
//   ENR_DIR defaults to ../.enrich/enr
//
// Only entries with at least one authMethod OR one setupStep are emitted, so a
// connector with no verifiable content does not get an (empty) Setup section.
import { readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { dirname, resolve, basename } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ENR_DIR = process.argv[2] || resolve(__dirname, '../.enrich/enr');
const OUT = resolve(__dirname, '../lib/connectors.enrichment.generated.ts');

const asArr = (v) => (Array.isArray(v) ? v : []);

// De-brand any Airbyte mention that leaked into researched prose. "Airbyte" in
// these contexts means the ETL runtime/platform, which here is pm; technical
// identifiers (airbyte_internal schema, _airbyte_ columns) get the pm prefix.
// Order matters: specific phrases before the generic word replacement.
function debrand(s) {
  let t = typeof s === 'string' ? s : '';
  // Fix the article BEFORE removing the word: "An Airbyte" -> "A pm" (pm starts
  // with a consonant sound), "an Airbyte" -> "a pm".
  t = t.replace(/\bAn(\s+)Airbyte\b/g, 'A$1pm');
  t = t.replace(/\ban(\s+)Airbyte\b/g, 'a$1pm');
  t = t.replace(/\bAirbyte Cloud\b/gi, 'a hosted environment');
  t = t.replace(/airbytehq\/airbyte/gi, 'owner/repo');
  t = t.replace(/airbyteio/gi, 'example');
  t = t.replace(/airbyte_internal/gi, 'pm_internal');
  t = t.replace(/_airbyte_/gi, '_pm_');
  // Final catch-all for any remaining form, incl. compounds/paths like
  // "airbyte_local" and "/tmp/airbyte_local".
  t = t.replace(/airbyte/gi, 'pm');
  return t.replace(/\s{2,}/g, ' ').trim();
}

function normalize(raw, slug) {
  // The Airbyte connector itself legitimately references Airbyte; exempt it
  // from de-branding and the airbyte-source filter.
  const isAirbyte = /airbyte/i.test(slug || '');
  const db = (s) => (isAirbyte ? (typeof s === 'string' ? s.trim() : '') : debrand(s));
  return {
    prerequisites: asArr(raw.prerequisites)
      .filter((s) => typeof s === 'string' && s.trim())
      .map((s) => db(s)),
    authMethods: asArr(raw.authMethods)
      .filter((m) => m && typeof m.name === 'string' && typeof m.summary === 'string')
      .map((m) => ({ name: db(m.name), summary: db(m.summary) })),
    setupSteps: asArr(raw.setupSteps)
      .filter((s) => s && typeof s.title === 'string' && typeof s.body === 'string')
      .map((s) => ({ title: db(s.title), body: db(s.body) })),
    // Drop any source that points at (or is titled after) Airbyte — vendor
    // sources only, no Airbyte links anywhere on the site (except the Airbyte connector).
    sources: asArr(raw.sources)
      .filter((s) => s && typeof s.title === 'string' && typeof s.url === 'string')
      .filter((s) => isAirbyte || !/airbyte/i.test(`${s.title} ${s.url}`))
      .map((s) => ({ title: s.title.trim(), url: s.url.trim() })),
  };
}

const files = readdirSync(ENR_DIR).filter((f) => f.endsWith('.json'));
const map = {};
let skippedEmpty = 0;
let skippedBad = 0;

for (const f of files.sort()) {
  const slug = basename(f, '.json');
  let raw;
  try {
    raw = JSON.parse(readFileSync(resolve(ENR_DIR, f), 'utf8'));
  } catch (e) {
    skippedBad++;
    console.warn(`  ! skip ${slug}: invalid JSON (${e.message})`);
    continue;
  }
  const e = normalize(raw, slug);
  if (e.authMethods.length === 0 && e.setupSteps.length === 0) {
    skippedEmpty++;
    continue;
  }
  map[slug] = e;
}

const slugs = Object.keys(map).sort();
const today = process.env.REVIEW_DATE || '';

const banner =
  `/**\n` +
  ` * Connector enrichment data — "Setup & Authentication" content.\n` +
  ` *\n` +
  ` * AUTO-GENERATED by scripts/merge-enrichment.mjs from the enrichment Workflow's\n` +
  ` * per-slug verified files (.enrich/enr/<slug>.json). DO NOT EDIT BY HAND.\n` +
  ` *\n` +
  ` * Every entry was researched (SearXNG + vendor docs) and then INDEPENDENTLY\n` +
  ` * adversarially verified: cited URLs must resolve, auth methods must map to a\n` +
  ` * real catalog secret field, and unconfirmable claims are pruned.\n` +
  (today ? ` *\n * REVIEWED: ${today}\n` : ``) +
  ` */\n\n`;

const type =
  `export type ConnectorEnrichment = {\n` +
  `  prerequisites: string[];\n` +
  `  authMethods: { name: string; summary: string }[];\n` +
  `  setupSteps: { title: string; body: string }[];\n` +
  `  sources: { title: string; url: string }[];\n` +
  `};\n\n`;

// Ordered, slug-keyed object literal.
const ordered = {};
for (const s of slugs) ordered[s] = map[s];

const body =
  banner +
  type +
  `export const CONNECTOR_ENRICHMENT: Record<string, ConnectorEnrichment> = ${JSON.stringify(ordered, null, 2)};\n\n` +
  `export const CONNECTOR_ENRICHMENT_COUNT = ${slugs.length};\n\n` +
  `export function connectorEnrichment(slug: string): ConnectorEnrichment | undefined {\n` +
  `  return CONNECTOR_ENRICHMENT[slug];\n` +
  `}\n`;

writeFileSync(OUT, body, 'utf8');

console.log(
  `Merged ${slugs.length} enrichment entries -> lib/connectors.enrichment.generated.ts ` +
    `(${(body.length / 1024).toFixed(0)} KB).\n` +
    `  inputs: ${files.length} files; skipped ${skippedEmpty} empty, ${skippedBad} invalid.`,
);
