// Generates website/lib/connectors.generated.ts from the Go connector catalog.
// Run: node scripts/gen-connectors.mjs   (or: npm run gen:connectors)
//
// Extracts unique connector display names from the authoritative catalog,
// attaches lightweight icon metadata, orders ~100 famous ones first, then the
// rest alphabetically.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const CATALOG = resolve(__dirname, '../../internal/connectors/catalog_data.json');
const ICON_DATA = resolve(__dirname, '../../internal/connectors/icon_data.json');
const OUT = resolve(__dirname, '../lib/connectors.generated.ts');

// ~100 well-known connectors to surface first (only those present in the
// catalog are used; missing ones are silently skipped).
const FAMOUS = [
  'GitHub', 'Stripe', 'Postgres', 'MySQL', 'MongoDB', 'Snowflake', 'BigQuery',
  'Redshift', 'Salesforce', 'HubSpot', 'Slack', 'Notion', 'Shopify', 'Linear',
  'Jira', 'Zendesk', 'Intercom', 'Google Sheets', 'Airtable', 'Mixpanel',
  'Amplitude', 'Segment', 'Datadog', 'Twilio', 'SendGrid', 'S3', 'Asana',
  'Monday', 'GitLab', 'Bitbucket', 'Confluence', 'PagerDuty', 'Klaviyo',
  'Mailchimp', 'Pipedrive', 'Close', 'Copper', 'Freshdesk', 'Freshsales',
  'Zoom', 'Google Analytics', 'Google Ads', 'Facebook Marketing', 'Instagram',
  'LinkedIn Ads', 'TikTok Marketing', 'Amazon Ads', 'Amazon Seller Partner',
  'Google Search Console', 'Greenhouse', 'Lever', 'BambooHR', 'Workday',
  'NetSuite', 'QuickBooks', 'Xero', 'Chargebee', 'Recurly', 'Braintree',
  'PayPal', 'Square', 'WooCommerce', 'BigCommerce', 'Magento', 'Klarna',
  'Typeform', 'SurveyMonkey', 'Calendly', 'Front', 'Help Scout', 'Drift',
  'Customer.io', 'Iterable', 'Braze', 'OneSignal', 'Twitter', 'Reddit Ads',
  'Snapchat Marketing', 'Pinterest', 'YouTube Analytics', 'Marketo',
  'ActiveCampaign', 'ConvertKit', 'Sentry', 'Rollbar', 'New Relic',
  'Statuspage', 'Okta', 'Auth0', 'Azure Blob Storage', 'Google Cloud Storage',
  'Dropbox', 'Box', 'Google Drive', 'Elasticsearch', 'ClickHouse', 'Oracle',
  'SQL Server', 'DynamoDB', 'Cassandra', 'Redis', 'Kafka',
];

const raw = JSON.parse(readFileSync(CATALOG, 'utf8'));
const iconRaw = JSON.parse(readFileSync(ICON_DATA, 'utf8'));
const items = (Array.isArray(raw) ? raw : (raw.connectors ?? raw.definitions ?? []))
  .filter((it) => it && it.slug && it.name);
const iconByConnector = new Map(
  (Array.isArray(iconRaw) ? iconRaw : [])
    .filter((icon) => icon && icon.connector)
    .map((icon) => [icon.connector, icon]),
);

const trim = (s) => (typeof s === 'string' ? s.trim() : '');
const validIconPath = (path) => /^icons\/[A-Za-z0-9._-]+\.svg$/.test(path);

function connectorRank(connector) {
  if (connector.type === 'source') return 0;
  if (connector.type === 'destination') return 1;
  return 2;
}

function preferredConnector(existing, candidate) {
  if (!existing) return candidate;
  const existingRank = connectorRank(existing);
  const candidateRank = connectorRank(candidate);
  if (candidateRank !== existingRank) {
    return candidateRank < existingRank ? candidate : existing;
  }
  return String(candidate.slug).localeCompare(String(existing.slug), 'en') < 0 ? candidate : existing;
}

function mapIcon(connector) {
  const icon = iconByConnector.get(connector.slug);
  if (!icon) return null;

  const path = trim(icon.path);
  if (!validIconPath(path)) {
    throw new Error(`Invalid connector icon path for ${connector.slug}: ${path}`);
  }

  return {
    id: trim(icon.id),
    path,
    publicPath: `/connectors/${path}`,
  };
}

// Unique display names. Prefer a source connector when source/destination
// variants share the same display name so the marketing list has one stable
// representative per name.
const connectorByName = new Map();
for (const item of items) {
  connectorByName.set(item.name, preferredConnector(connectorByName.get(item.name), item));
}

const allNames = [...connectorByName.keys()];
const nameSet = new Set(allNames);

const famousPresent = FAMOUS.filter((n) => nameSet.has(n));
const famousSet = new Set(famousPresent);
const rest = allNames
  .filter((n) => !famousSet.has(n))
  .sort((a, b) => a.localeCompare(b, 'en', { sensitivity: 'base' }));

const ordered = [...famousPresent, ...rest];
const connectorItems = ordered.map((name) => {
  const connector = connectorByName.get(name);
  return {
    name,
    icon: mapIcon(connector),
  };
});

const banner = `// AUTO-GENERATED by scripts/gen-connectors.mjs — DO NOT EDIT.
// Source: internal/connectors/catalog_data.json
// Run \`npm run gen:connectors\` to regenerate.
`;

const body =
  banner +
  `\nexport type ConnectorListItem = {\n` +
  `  name: string;\n` +
  `  icon: { id: string; path: string; publicPath: string } | null;\n` +
  `};\n\n` +
  `export const CONNECTORS: ConnectorListItem[] = ${JSON.stringify(connectorItems, null, 0)};\n` +
  `\nexport const CONNECTOR_COUNT = ${connectorItems.length};\n` +
  `export const CONNECTOR_CATALOG_COUNT = ${items.length};\n`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, body, 'utf8');

console.log(
  `Wrote ${connectorItems.length} connectors to lib/connectors.generated.ts ` +
    `(${famousPresent.length} famous first, ${rest.length} alphabetical).`,
);
